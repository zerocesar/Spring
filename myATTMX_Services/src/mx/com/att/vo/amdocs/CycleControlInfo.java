package mx.com.att.vo.amdocs;

public class CycleControlInfo {
	private short cycleCode;
	private short cycleMonth;
	private short cycleYear;
	
	public short getCycleCode() {
		return cycleCode;
	}
	public void setCycleCode(final short cycleCode) {
		this.cycleCode = cycleCode;
	}
	public short getCycleMonth() {
		return cycleMonth;
	}
	public void setCycleMonth(final short cycleMonth) {
		this.cycleMonth = cycleMonth;
	}
	public short getCycleYear() {
		return cycleYear;
	}
	public void setCycleYear(final short cycleYear) {
		this.cycleYear = cycleYear;
	}
}

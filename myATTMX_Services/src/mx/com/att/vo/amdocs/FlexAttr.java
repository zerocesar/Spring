package mx.com.att.vo.amdocs;

public class FlexAttr {
	private String name;
	private FlexAttrValue value;
	public final String getName() {
		return name;
	}
	public final void setName(final String name) {
		this.name = name;
	}
	public final FlexAttrValue getValue() {
		return value;
	}
	public final void setValue(final FlexAttrValue value) {
		this.value = value;
	}
}

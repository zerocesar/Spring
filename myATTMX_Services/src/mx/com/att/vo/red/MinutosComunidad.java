package mx.com.att.vo.red;

public class MinutosComunidad {
	private String minutosComunidadIncluido;
	private String minutosComunidadExtra;
	private String minutosComunidadRegalo;
	private String minutosComunidadConsumidos;
	
	public String getMinutosComunidadRegalo() {
		return minutosComunidadRegalo;
	}
	public void setMinutosComunidadRegalo(String minutosComunidadRegalo) {
		this.minutosComunidadRegalo = minutosComunidadRegalo;
	}
	public String getMinutosComunidadIncluido() {
		return minutosComunidadIncluido;
	}
	public void setMinutosComunidadIncluido(String minutosComunidadIncluido) {
		this.minutosComunidadIncluido = minutosComunidadIncluido;
	}
	public String getMinutosComunidadExtra() {
		return minutosComunidadExtra;
	}
	public void setMinutosComunidadExtra(String minutosComunidadExtra) {
		this.minutosComunidadExtra = minutosComunidadExtra;
	}
	public String getMinutosComunidadConsumidos() {
		return minutosComunidadConsumidos;
	}
	public void setMinutosComunidadConsumidos(String minutosComunidadConsumidos) {
		this.minutosComunidadConsumidos = minutosComunidadConsumidos;
	}
	
	
}

package mx.com.att.vo.red;

public class DatosFocalizacion {
	private String idOperador;
	private String servicio;
	private String subservicio;
	private String tecnologia;
	private String isPostpagoOrHibrido;
	private String fechaCorte;
	private String idPrepago;
	public String getIdOperador() {
		return idOperador;
	}
	public void setIdOperador(String idOperador) {
		this.idOperador = idOperador;
	}
	public String getServicio() {
		return servicio;
	}
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	public String getSubservicio() {
		return subservicio;
	}
	public void setSubservicio(String subservicio) {
		this.subservicio = subservicio;
	}
	public String getTecnologia() {
		return tecnologia;
	}
	public void setTecnologia(String tecnologia) {
		this.tecnologia = tecnologia;
	}
	public String getIsPostpagoOrHibrido() {
		return isPostpagoOrHibrido;
	}
	public void setIsPostpagoOrHibrido(String isPostpagoOrHibrido) {
		this.isPostpagoOrHibrido = isPostpagoOrHibrido;
	}
	public String getFechaCorte() {
		return fechaCorte;
	}
	public void setFechaCorte(String fechaCorte) {
		this.fechaCorte = fechaCorte;
	}
	public String getIdPrepago() {
		return idPrepago;
	}
	public void setIdPrepago(String idPrepago) {
		this.idPrepago = idPrepago;
	}
}
